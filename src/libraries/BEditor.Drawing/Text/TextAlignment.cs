namespace BEditor.Drawing
{
    /// <summary>
    /// Defines how text is aligned.
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// The text is left-aligned.
        /// </summary>
        Left = 0,

        /// <summary>
        /// The text is centered.
        /// </summary>
        Center = 1,

        /// <summary>
        /// The text is right-aligned.
        /// </summary>
        Right = 2,
    }

}
